# =========================================
# 1. IMPORT LIBRARIES
# =========================================
import streamlit as st
import pandas as pd
import warnings
from mlxtend.frequent_patterns import fpgrowth, association_rules

warnings.filterwarnings("ignore")

# =========================================
# 2. LOAD DATA
# =========================================
import os
@st.cache_data
def load_data():
    current_dir = os.path.dirname(os.path.abspath(__file__))
    file_path = os.path.join(current_dir, "raga_notes_binary_encoding_935.csv")
    df_original = pd.read_csv(file_path)
    return df_original

df_original = load_data()
raga_names = df_original['Raga']

# =========================================
# 3. DATA PREPROCESSING
# =========================================
df = df_original.copy()
df = df.drop(columns=['Raga', 'S', 'F1'], errors='ignore')

swara_map = {
    "F2": "R1", "F3": "R2", "F4": "R3",
    "F5": "G1", "F6": "G2", "F7": "G3",
    "F8": "M1", "F9": "M2",
    "F10": "P",
    "F11": "D1", "F12": "D2", "F13": "D3",
    "F14": "N1", "F15": "N2", "F16": "N3"
}

df = df.rename(columns={k: v for k, v in swara_map.items() if k in df.columns})
df = df.astype(bool)

# =========================================
# 4. ASSOCIATION RULE MINING
# =========================================
@st.cache_data
def generate_rules(df):
    frequent_itemsets = fpgrowth(df, min_support=0.3, use_colnames=True)

    rules = association_rules(
        frequent_itemsets,
        metric="confidence",
        min_threshold=0.6
    )

    rules = rules.dropna()
    rules = rules.sort_values(by="confidence", ascending=False).head(50)

    rules['antecedents'] = rules['antecedents'].apply(list)
    rules['consequents'] = rules['consequents'].apply(list)

    return rules

rules = generate_rules(df)

# =========================================
# 5. PRECOMPUTE SWARA SETS
# =========================================
raga_swara_sets = [
    set(df.columns[df.iloc[i] == True])
    for i in range(len(df))
]

# =========================================
# 6. FUNCTIONS
# =========================================
def recommend_swaras(input_swaras):
    input_set = set(input_swaras)
    recommendation_dict = {}

    for _, row in rules.iterrows():
        if set(row['antecedents']).issubset(input_set):
            for swara in row['consequents']:
                if swara not in recommendation_dict or row['confidence'] > recommendation_dict[swara]:
                    recommendation_dict[swara] = row['confidence']

    recommendations = [(k, v) for k, v in recommendation_dict.items()]
    recommendations.sort(key=lambda x: x[1], reverse=True)

    return recommendations[:5]


def predict_raga(input_swaras):
    input_set = set(input_swaras)
    scores = []

    for i, raga_swaras in enumerate(raga_swara_sets):
        remaining_swaras = raga_swaras - input_set

        intersection = len(input_set & raga_swaras)
        union = len(input_set | raga_swaras)

        score = intersection / union if union != 0 else 0

        penalty = len(remaining_swaras) * 0.05
        final_score = score - penalty

        scores.append((raga_names.iloc[i], final_score))

    scores.sort(key=lambda x: x[1], reverse=True)

    return scores[:5]

# =========================================
# 7. STREAMLIT UI
# =========================================
st.title("🎵 Raga Prediction using Swaras")

st.write("Enter swaras separated by comma (Example: R2,M1,P,N2)")

user_input = st.text_input("Enter Swaras")

if st.button("Predict"):

    if user_input:
        input_swaras = [s.strip() for s in user_input.split(',') if s.strip()]

        # Recommendation
        recommended = recommend_swaras(input_swaras)

        st.subheader("🎶 Next Swara Prediction")
        if recommended:
            for swara, conf in recommended:
                st.write(f"Recommend **{swara}** (confidence={conf:.2f})")

        # Add best swara
        if recommended:
            input_swaras.append(recommended[0][0])

        # Raga prediction
        result = predict_raga(input_swaras)

        st.subheader("🎼 Predicted Raga")
        for raga, score in result:
            st.write(f"{raga} → Match Score: {score:.2f}")

        # Final result
        st.success(f"🎯 Final Predicted Raga: {result[0][0]}")

    else:
        st.warning("Please enter swaras!")